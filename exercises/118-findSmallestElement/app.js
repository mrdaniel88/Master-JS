function findSmallestElement(arr) {
    // your code here
    if (arr.length < 1) return 0;
    smallest = 1000000000000000000;
    for (number of arr) {
        number < smallest ? smallest = number : null;
    }
    return smallest
}

let output = findSmallestElement([4, 1, 9, 10]);
